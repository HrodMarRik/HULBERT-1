<h1 class="crayon rubrique-titre-67 "><img src="IMG/logo/rubon67.svg?1637496507" alt="Stéganographie" width="48" height="48" class="vmiddle" itemprop="image">&nbsp;<span itemprop="headline name">Stéganographie</span></h1>
<h3 class="crayon rubrique-descriptif-67 " itemprop="description">Si la cryptographie est l’art du secret, la stéganographie est l’art de la dissimulation&nbsp;: l’objet de la stéganographie n’est pas de rendre un message inintelligible aux personnes non autorisées mais de le faire passer inaperçu.
</h3>
<div class="texte crayon rubrique-texte-67 " itemprop="text"><p>La "bonne" stéganographie tient compte de deux facteurs&nbsp;: COMSEC pour communications security et TRANSEC pour transport security. Ces épreuves consistent à retrouver une information qui a été dissimulée dans une donnée informatique&nbsp;: une image, un son, une vidéo...</p>
<p>Prérequis&nbsp;:</p>
<p><span class="spip-puce ltr"><b>–</b></span>&nbsp;Connaitre les formats de représentation de données&nbsp;;
<br><span class="spip-puce ltr"><b>–</b></span>&nbsp;Maitriser un langage de script&nbsp;;
<br><span class="spip-puce ltr"><b>–</b></span>&nbsp;Avoir de l’imagination.</p></div>
